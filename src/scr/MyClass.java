package scr;

public class MyClass {
    public int addNumber(int a, int b){
        return a+b;
    }
    public boolean findPrimeNumber(int a){
        int value=a;
        for(int i=1;i<=value;i++){
            int result=value%i;
            if(0<result)break;


        }
        return false;
    }
}
